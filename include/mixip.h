#include <stdio.h>
#include <stdint.h>
#include <semaphore.h>
#include <serialposix.h>

/***************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************
 * Data structures
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/

typedef struct{
  uint8_t network;
  sem_t   tx_wait;
  sem_t   tx_done;
  sem_t   rx_wait;
  sem_t   rx_done;
} flow_t;
  
typedef struct{
  uint8_t  data[ BUFSIZ ];             // Buffer that will contain the serial port received information
  size_t   len;                        // Current length of data
  sem_t    available;                  // Semaphore used to advertise that information is available
  sem_t    empty;                      // Semaphore used to stop a process while the other is processing the information
} buffer_t ;

typedef struct{
  uint8_t  boot:1;                     // Flag to start the ser2eth, if the driver does not start it the eth2ser will be stucked 
  uint8_t  size_rb;                    // Packet oriented ring buffer size in slots
  uint8_t  size_sls;                   // Size of the serial link segment
} translator_parameters_t;

/***************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************
 * Prototypes
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Halts communications to the serial port via dread and dwrite.\n
 *        Its designed to be used for sending specific configuration commands to the serial port on the dloop function. \n
 *        Basically gives total authority over the serial port to the function calling it from dloop scope.
 *  
 * @param[in] flow A structure composed of semaphores to halt the other processes. 
 * 
 * @return Upon success, halting the dread and dwrite, it returns 0. \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 * 
 * @b Example \n
 *  Halting the system to change device configurations: \n
 * @code{.c}
 * int 
 * dloop( flow_t * flow ){
 *   mixip_halt( flow );
 *   change_config( ... );
 *   mixip_continue( flow );
 *   // ...  
 * }
 * @endcode
 *  
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_halt( flow_t * flow );

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Continues communications to the serial port via dread and dwrite.\n
 *        Its designed to be used after sending specific configuration commands to the serial port on the dloop function.
 *  
 * @param[in] flow A structure composed of semaphores to halt the other processes. 
 * 
 * @return Upon success, halting the dread and dwrite, it returns 0. \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 * 
 * @b Example \n
 *  Halting the system to change device configurations: \n
 * @code{.c}
 * int 
 * dloop( flow_t * flow ){
 *   mixip_halt( flow );
 *   change_config( ... );
 *   mixip_continue( flow );
 *   // ...  
 * }
 * @endcode
 *  
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_continue( flow_t * flow );

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Change the size of the serial link segment from the MIXIP-Translator.
 *  
 * @param[in] size The size between 5 to 255. Note that the overhead is equal to 4 bytes, so if size is 5 the payload will be equal to 1.
 * 
 * @return Upon success, the serial link segment will be updated. \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 *  - `ENOTSUP`: Invalid configuration
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_translator_serial_link_segment_size(  const uint8_t size, translator_parameters_t * param );

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Change the size of the packet-roiented ring buffer from the MIXIP-Translator. 
 *  
 * @param[in] size The size between 1 to 255.
 * 
 * @return Upon success, the ring buffer will be updated. \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 *  - `ENOTSUP`: Invalid configuration
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_translator_ring_buffer_size( const uint8_t size, translator_parameters_t * param  );

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Activate the MIXIP-Translator. 
 * 
 * @return Upon success, the MIXIP-Translator will be activated \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_translator_activate( translator_parameters_t * param );

/**********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************//**
 * @brief Connect to the MIXIP-Translator. 
 * 
 * @return Upon success, the MIXIP-Translator will connect. \n
 *         Otherwise, -1 is returned and `errno` is set to indicate the error.
 * 
 *  - `EINVAL`: Invalid argument
 **************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
int8_t mixip_translator_connect( const char * driver_name, translator_parameters_t * param );

